        //         int productId = Integer.valueOf(productComboBox.getSelectedItem());
        //
        //         productId.setText(productId);
        productId.setEnabled(false);//to disable the field
        int pid = (int)productComboBox.getSelectedItem();

        Product prod = this.delPackage.findProduct(pid);
        this.productViewd = prod;
        //display the product content
        if(!prod.equals(null)){
            productId.setText(String.valueOf(pid));
            productName1.setText(prod.getProductName());
            productPrice1.setText(String.valueOf(prod.getPrice()));
        }else{
            JOptionPane.showMessageDialog(null, "Product Not Found!");
//               JOptionPane.showMessageDialog(null, "Saved!");
        }

